console.log('Loop For');

for (let i = 0; i < 10; i++) {
    console.log(i);
}

console.log('Loop While');

let j = 0;
while (j < 10) {
    console.log(j);
    j++;
}

console.log('Loop Do-While');

let k = 0;
do {
    console.log(k);
    k++;
} while (k < 10);

console.log('Loop for 100-0');
for (let i = 100; i >= 0; i -= 10) {
    console.log(i);
};

console.log('Loop While 100-0');

let m = 100;
while (m >= 0) {
    console.log(m);
    m -= 10;
}

console.log('Loop Do-While 100-0');

let n = 100;
do {
    console.log(n);
    n -= 10;
} while (n >= 0);
